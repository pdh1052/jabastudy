package com.league;

public class Skill {
	
	public Skill() {
	}
	
	private String Q;
	private String W;
	private String E;
	private String R;
	
	
	
	public Skill(String q, String w, String e, String r) {
		
		Q = q;
		W = w;
		E = e;
		R = r;
	}



	public String getQ() {
		return Q;
	}



	public void setQ(String q) {
		System.out.println(q+"쓴다");
		Q = q;
		
	}



	public String getW() {
		return W;
	}



	public void setW(String w) {
		System.out.println(w+"쓴다");
		W = w;
	}



	public String getE() {
		return E;
	}



	public void setE(String e) {
		System.out.println(e+"쓴다");
		E = e;
	}



	public String getR() {
		return R;
	}



	public void setR(String r) {
		System.out.println(r+"쓴다");
		R = r;
	}
	
	
	

}








